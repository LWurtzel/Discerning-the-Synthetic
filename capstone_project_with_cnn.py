import tensorflow as tf
AUTOTUNE = tf.data.AUTOTUNE
IMG = (32, 32); BATCH = 64

train_ds = tf.keras.utils.image_dataset_from_directory(
    '/content/CIFAKE/train', label_mode='binary', image_size=IMG,
    batch_size=BATCH, validation_split=0.2, subset='training', seed=42)
val_ds = tf.keras.utils.image_dataset_from_directory(
    '/content/CIFAKE/train', label_mode='binary', image_size=IMG,
    batch_size=BATCH, validation_split=0.2, subset='validation', seed=42)
test_ds = tf.keras.utils.image_dataset_from_directory(
    '/content/CIFAKE/test', label_mode='binary', image_size=IMG,
    batch_size=BATCH, shuffle=False)

train_ds = train_ds.cache().shuffle(1000).prefetch(AUTOTUNE)
val_ds   = val_ds.cache().prefetch(AUTOTUNE)
test_ds  = test_ds.cache().prefetch(AUTOTUNE)

from tensorflow.keras import layers
from tensorflow.keras.models import Sequential

model = Sequential()
model.add(layers.Input(shape=(32, 32, 3)))
model.add(layers.Rescaling(1./255))
model.add(layers.RandomFlip('horizontal'))

model.add(layers.Conv2D(64, (3, 3), activation='relu'))
model.add(layers.Conv2D(128, (3, 3), activation='relu'))
model.add(layers.Conv2D(256, (3, 3), activation='relu'))
model.add(layers.MaxPooling2D((2, 2)))
model.add(layers.Dropout(0.25))

model.add(layers.Conv2D(64, (3, 3), activation='relu'))
model.add(layers.MaxPooling2D((2, 2)))
model.add(layers.Dropout(0.25))

model.add(layers.Flatten())
model.add(layers.Dense(128, activation='relu'))
model.add(layers.Dropout(0.5))
model.add(layers.Dense(1, activation='sigmoid'))

model.compile(loss='binary_crossentropy', optimizer='adam', metrics=['accuracy'])

import pandas as pd
results = []

def log_run(name, model, history):
    test_loss, test_acc = model.evaluate(test_ds, verbose=0)
    val_acc = max(history.history['val_accuracy'])
    results.append({'model': name,
                    'val_acc': round(val_acc, 4),
                    'test_acc': round(test_acc, 4),
                    'epochs_run': len(history.history['loss'])})
    return pd.DataFrame(results)

from tensorflow.keras.callbacks import EarlyStopping

early_stop = EarlyStopping(monitor='val_loss', patience=5,
                           restore_best_weights=True, verbose=1)
history = model.fit(train_ds, validation_data=val_ds, epochs=40,
                    callbacks=[early_stop])

score = model.evaluate(test_ds, verbose=1)
print('Test loss:', score[0])
print('Test accuracy:', score[1])

log_run('wide_64-128-256_long', model, history)

