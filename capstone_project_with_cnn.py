import glob, random
import numpy as np
from PIL import Image
import matplotlib.pyplot as plt
import ipywidgets as widgets
from IPython.display import display, clear_output

def play_game(test_dir, model, rounds=10, display_size=512):
    pool = ([(f, 'REAL') for f in glob.glob(f'{test_dir}/REAL/*')] +
            [(f, 'FAKE') for f in glob.glob(f'{test_dir}/FAKE/*')])
    random.shuffle(pool)
    S = {'i': 0, 'human': 0, 'machine': 0, 'played': 0}
    name = {0: 'FAKE', 1: 'REAL'}

    img_out, res_out = widgets.Output(), widgets.Output()
    b_real = widgets.Button(description='Real', button_style='success')
    b_fake = widgets.Button(description='Fake', button_style='danger')
    b_skip = widgets.Button(description='Skip')
    b_next = widgets.Button(description='Next ▶', button_style='info', disabled=True)
    controls = widgets.HBox([b_real, b_fake, b_skip, b_next])

    def show_image():
        with img_out:
            clear_output(wait=True)
            path, _ = pool[S['i']]
            img = Image.open(path).convert('RGB')
            plt.figure(figsize=(4, 4))
            plt.imshow(img.resize((display_size, display_size), Image.LANCZOS))
            plt.axis('off'); plt.title(f"Round {S['played']+1} of {rounds}")
            plt.show()

    def toggle(guessing):
        b_real.disabled = b_fake.disabled = b_skip.disabled = not guessing
        b_next.disabled = guessing

    def guess(choice):
        path, truth = pool[S['i']]
        img = Image.open(path).convert('RGB')
        arr = np.array(img.resize((32, 32)), dtype='float32')[None, ...]
        prob = float(model.predict(arr, verbose=0)[0][0])
        m_pred = name[int(prob >= 0.5)]
        h_ok, m_ok = choice == truth, m_pred == truth
        S['human'] += h_ok; S['machine'] += m_ok; S['played'] += 1
        with res_out:
            clear_output(wait=True)
            print(f"Truth:  {truth}")
            print(f"You:    {choice}   {'✓' if h_ok else '✗'}")
            print(f"Model:  {m_pred}  (p={prob:.2f})   {'✓' if m_ok else '✗'}")
            print(f"Score — You: {S['human']}/{S['played']}   Model: {S['machine']}/{S['played']}")
        toggle(False)

    def advance(_=None):
        S['i'] += 1
        if S['played'] >= rounds or S['i'] >= len(pool):
            controls.close()
            with img_out: clear_output()
            with res_out:
                clear_output(wait=True)
                print(f"FINAL — You: {S['human']}/{S['played']}   Model: {S['machine']}/{S['played']}")
        else:
            toggle(True); show_image()

    b_real.on_click(lambda b: guess('REAL'))
    b_fake.on_click(lambda b: guess('FAKE'))
    b_skip.on_click(advance)          # skip: no guess, just move on
    b_next.on_click(advance)

    display(img_out, controls, res_out)
    show_image()

play_game('CIFAKE/test', model, rounds=10)